*
*ROCKET LAUNCHER EXPLOSION ROUTINE
*A8 = PTR TO OBJECT THAT GOT HIT
*A9 = OFLAGS:OZPOS OF ROCKET
*A10 = OID OF ROCKET
*A11 = YX POSITION OF ROCKET
REXPLODE:
	MOVI	REXINIT,A14
