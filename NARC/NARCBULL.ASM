	MOVE	*A8(OFLAGS),A2,W	;FLAGS PLEASE
	MOVE	A9,A3			;Z POSITION OF SMOKE TRAIL SHOULD
	SUBK	2,A3			;BE LESS THAN THAT OF THE EXPLOSION.

