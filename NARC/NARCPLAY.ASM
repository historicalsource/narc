	MMTM	SP,A0,A1,A2,A3,A6,A7,A10,A13,A14
	MOVE	@GAMSTATE,A0,W
	CMPI	INAMODE,A0
	JREQ	CKLCHKX
	CMPI	INPLAY,A0
	JRNE	CKBNDX			;NOT IN PLAY, DON'T WORRY ABOUT IT
