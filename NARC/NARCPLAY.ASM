* ISCHOP - IS THIS OBJECT THE CHOPPER?					 *
* A1 = PTR TO THE OBJECT						 *
* RETURNS:								 *
* 	Z BIT SET(EQ) = YES						 *
* 	Z BIT CLR(NE) = NO						 *
*                                                                        *
**************************************************************************
ISCHOP
	MMTM	SP,A0,A1
	MOVE	@PLAYCAR,A1,L		;GET THE CAR POINTER
	JRZ	ISCHOPX
	MOVE	*A1(OID),A0,W
	CMPI	TRAN2OID,A0		;COMPARE TO THE CHOPPER I.D.	
