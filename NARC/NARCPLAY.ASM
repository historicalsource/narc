	MOVE	*A8(OZPOS),A3,W		;GET THIS GUYS Z POSITION
	MOVE	A2,A2
	JRN	CKUBND			;BR = NEGATIVE, CHECK YMIN
***	MOVE	*A8(OZPLUS),A7,W
***	ADD	A7,A3
