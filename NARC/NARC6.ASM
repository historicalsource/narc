*
*LOFFREDO DISPATCHER
*
*EUGDISP
* 	MOVI	7,A0
*	MOVE	A0,@LOFMAX,W		;MAX LOF COUNT
*	MOVI	100,A0
*	MOVE	A0,@LOFFIRE,W		;LOFFREDO FIRE PROBABILITY
*	CREATE	0,DASDIS3
*	RETS
*DASDIS3
*	CLR	A5
*	MOVI	>00450E10,A14
*	CALLR	LOFCOLD
*	SLOOP	320,DASDIS3		;WAIT FOR THEM TO DIE
*
*LOAF COLD START
*A5=Y:X COORD; IF ZERO THEN MAKE-UP COORDINATE
*A6=0:Z COORD
*A14= 0; FIX NUMBER:RANDOM NUMBER; 0E; ENEMY TYPE:ENEMY SUBTYPE (BYTE FIELDS)
*
LOFCOLD:
	MMTM	SP,A0,A5,A11,A14
	MOVE	A14,A10			;SAVE OID
	MOVE	A5,A9
	MOVE	A14,A2	
	SLL	8,A2			;GET FIXED COUNT
	SRL	28,A2
	MOVE	A14,A0
	SLL	12,A0			;GET RANDOM COUNT
	SRL	28,A0
	JRZ	LOFCLD0			;BR = NO RANDOM GUYS
	CALLA	RANDU
	ADD	A0,A2
LOFCLD0
	MOVE	A14,A11			;GET SUBTYPE CODE
	SLL	28,A11
	SRL	28,A11	

*CHECK BOZO MODE
	CMPI	5,A11
	JRNE	LOFCLD00   		;RED DUDE?
	MOVE	@WAVE,A0,W
	CMPI	1,A0			;WAVE 1?
	JRNE	LOFCLD00
	MOVI	P1DATA,A0	       
	CALLR	BOZCHK
	JRC	LFCLD000
	MOVI	P2DATA,A0	       
	CALLR	BOZCHK
	JRNC	LOFCLD00
LFCLD000
	MOVI	3,A11			;CONVERT TO GREENER
LOFCLD00
	MOVI	>B0,A0
	MPYU	A0,A11
	ADDI	LTTAB,A11		;FORM TABLE REFERENCE BASE ADDR
LOFCLD1:
        CREATE	LOFPID,DAS1X		;MAKE 'EM
	DSJS	A2,LOFCLD1
	MMFM	SP,A0,A5,A11,A14
	RETS
*
*BOZO CHECK
*A0=PDATA POINTER
*CS IF PLAYER IS A BOZO
*
BOZCHK
	MMTM	SP,A1
	MOVE	*A0(POBJ),A1,L
	JREQ	BOZFAIL
	MOVE	*A0(PBUYINS),A1,W
	CMPI	IBUYINS,A1
	JRNE	BOZCK1			;BUYIN BOZO
	MOVE	*A0(PLIVES),A1,W
	CMPI	1,A1
	JRHI	BOZFAIL
BOZCK1
	MOVE	*A0(PBUSTS),A1,W
	CMPI	8,A1
	JRLS	BOZOK
BOZFAIL
	CLRC
	MMFM	SP,A1
	RETS
BOZOK
	SETC
	MMFM	SP,A1
	RETS
