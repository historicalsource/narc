*
*LOFREDO SHOOT
*A8=OBJ, A3=ABS DX, A11=PLAYER
*RETURNS CC= NO SHOT, CS = SHOOT
*
LOFSHOOT:
	MOVE	@WAVE,A0,W
	CMPI	1,A0		;WAVE 1?
	JRNE	LOFSH00		;NO
	MOVE	A11,A0		;TARGET PLAYER IN A0
	CALLA	GETPLYR
	CALLR	BOZCHK
	JRNC	LOFSH00		;NO BOZO TARGET
	MOVE	@LOFFIRE,A0,W
	SRL	1,A0	
	JRUC	LOFSH01		;SHOOT HALF AS OFTEN
LOFSH00
	MOVE	@LOFFIRE,A0,W
LOFSH01
	CALLA	RANDPER
	JRNC	LOFSHX		;NO GO
	CMPI	30,A3		;TOO CLOSE FOR A SHOT?
	JRLS	LOFSHX
	CALLA	ZCHK
	MOVI	LOFSS,A9

	MOVE	*A13(ETEMP1),A0,W	;GO FOR A SQUAT SHOT?
	CALLA	RANDPER
	JRNC	LOFSH0		;NO
	CMPI	40,A3		;TOO CLOSE FOR A DOWNER?
	JRLS	LOFSH0
	MOVI	LOFKS,A9
	JRUC	LOFSH1
LOFSH0:
	MOVE	*A13(ETEMP1+>10),A0,W	;GO FOR A DOWN SHOT?
	CALLA	RANDPER
	JRNC	LOFSH1		;NO
	CMPI	70,A3		;TOO CLOSE FOR A DOWNER?
	JRLS	LOFSH1
	MOVI	LOFSL,A9
LOFSH1:
	CALLA	PSTOP
	MOVE	A11,A0
	CALLA	FACEPLYR		;FACE THE PLAYER BOYS
	MOVI	1,A1
	JSRP	FRANIM	     
	SETC
	RETP
LOFSHX:
	CLRC
	RETP
*
*LOAFER ON A CHOPPER
*A8  = LOF
*A10 = CHOPPER MAIN
*A11 = PLAYER SELECTED
*
LOFCHOP:
*FIND A CHOPPER
	MOVE	@CARLIST,A10,L
	JREQ	LOFLOOP0
CSCL1
	MOVE	*A10(OID),A1,W
	CMPI	CAR3OID|CBODYID,A1
	JREQ	CSCLX

	MOVE	*A10(OSLINK),A10,L
	JRNE	CSCL1
	JRUC	LOFLOOP0		
CSCLX
	CLR	A0   		;COUNT CHOPPER LOFS
	MOVE	@ENEMIES,A1,L
	JREQ	CSCL3
CSCL20
	MOVE	*A1(OID),A2,W
	CMPI	>0E18,A2
	JRNE	CSCL200
	INC	A0
CSCL200
	CMPI	>0E17,A2
	JRNE	CSCL2
	INC	A0
CSCL2	MOVE	*A1(OSLINK),A1,L
	JRNE	CSCL20
CSCL3	
	DEC	A0   			;COMPUTE OFFSET
	MOVI	21,A1
	MPYU	A1,A0
	MOVI	6,A0
	CALLA	SRAND
	SUBK	4,A0
	ADD	A1,A0
	MOVE	A0,*A13(ETEMP4),W	;OFFSET FROM CENTER
	MOVI	LOFLY,A9		;MAKE THE DUDE FLY
	MOVK	4,A1
	JSRP	FRANIM
LOFCHLP:
	CALLR	LOFCH1
	CALLA	SCRTST		;ONLY JUMP ON SCREEN
	JRNE	LOFCHSL
	MOVI	4,A0		;JUMP EVERY NOW AND THEN FOR FUN
	MOVE	*A8(OID),A1,W
	CMPI	>E18,A1
	JRLO	LOFCHS7
	MOVE	*A8(OXVEL+>10),A1,W
	JRLE	LOFCHS7
	MOVE	@WORLDTL,A1,W		;COME FROM EDGE OF SCREEN
	ADDI	80,A1
	MOVE	*A8(OXPOS),A2,W
	CMP	A2,A1
	JRHS	LOFCHS7
	MOVI	999,A0	       	;JUMPERS REALLY GO FOR IT!!!!
LOFCHS7
	CALLA	RANDPER
	JRC	LDOWN
	JSRP	LCHFIRE
LOFCHSL
	SLEEP	2
	JRUC	LOFCHLP
