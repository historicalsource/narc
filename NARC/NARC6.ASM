	MOVE	*A11(ANIRATE),A0,W	;GET SLEEP TIME
	MOVE	A0,*A13(ETEMP3),W

	
	MOVE 	*A8(OID),A0,W
	SLL	28,A0
	SRL	28,A0
	CMPI	7,A0
	JRHS	LOFCHOP		;CHOPPER DUDE

	MOVI	120,A0
	CALLA	RANDPER
	JRNC	LOFLOOP0
	CALLA	SELCTPLY	;TARGET PLAYER IN A11
	JSRP	LOFHANG		;HANG OUT FOR A BIT

