LOFHANG:
	CALLA	PSTOP
	MOVI	4,A0
	CALLA	RANDU
	DEC	A0
	SLL	5,A0
	ADDI	HANGTAB,A0
	MOVE	*A0,A9,L	;GET YOUR HANG OUT FRAME
