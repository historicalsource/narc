CHROCK
	MMTM	SP,A1,A7,A8,A10
	CALLA	CARSCRTST
	JRNZ	CHROCKX		   	;NZ = OFF SCREEN	 

	MOVE	@P1DATA+POBJ,A0,L	;LOOKING FOR PLAYER 1
	CALLR	TARGPLY			;SEE IF PLAYER IN SIGHT
