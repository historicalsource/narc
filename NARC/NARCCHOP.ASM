**************************************************************************
*                                                                        *
* CHOPCOLD - COLD START THE CHOPPER					 *
*                                                                        *
**************************************************************************
CHOPCOLD
	MOVE	@CHOPCNT,A0,W
	JRNE	CHOPCX
	CREATE	CHOPEPID,CHOPPER
CHOPCX
	RETS

**************************************************************************
*                                                                        *
* CHOPDISP - THE CHOPPER DISPATCHER.					 *
*                                                                        *
**************************************************************************

CHOPST	CREATE	CHOPEPID,CHOPDISP
	RETS

CHOPDSPL
	SLEEP	30
	MOVE	@CHOPCNT,A0,W
	JRNE	CHOPDSPL
	MOVI	>240,A0		;RANDOM SLEEP TIME
	CALLA	RANDU
	ADDI	>140,A0
	CALLA	PRCSLP
CHOPDISP
	CREATE	CHOPEPID,CHOPPER1
	JRUC	CHOPDSPL

**************************************************************************
*                                                                        *
* CHOPPER-THE CHOPPER PROCESS!!!					 *
* A8	 = DRIVER							 *	
* A10	 = MAIN BODY							 *
* ETEMP1 = PTR TO SKIDS OBJECT						 *
* ETEMP2 = PTR TO TAIL OBJECT						 *
* ETEMP3 = PTR TO EXTENSION OBJECT					 *
* ETEMP4 = PTR TO BLADE OBJECT						 *
* ETEMP5 = PTR TO ROTOR OBJECT						 *
* ETEMP7 = PTR TO BLADE OBJECT, MIRRORED				 *
*                                                                        *
**************************************************************************
CHOPPER1
	MOVI	CHOPINT1,A14
	JRUC	CHOPPER2
CHOPPER
	MOVI	CHOPINIT,A14
CHOPPER2
	MOVI	D3TAB,A6						 
	CALLA	CARINIT			;INITIALIZE THE CAR
	JAZ	DUMDIE			;BR = INITIALIZATION FAILURE

	MOVK	20,A3	      		;BULLET HIT COUNT
	MOVB	A3,*A13(ECLIP)

	CLR	A3
	MOVE	A3,*A10(OPLINK),L
	MOVE	*A10(OZVAL),A3,L
	MOVE	*A13(ETEMP1),A6,L
	MOVE	A10,*A13(ETEMP1),L
	MOVE	A6,A10
	MOVE	A3,*A10(OZVAL),L
	MOVE	A13,*A10(OPLINK),L	;BODY
	MOVE	A13,*A8(OPLINK),L	;PILOT

***	CALLR	CHOPZRST		;RESET THE CHOPPER Z'S
	CALLR	CHOPFLIP
	CALLR	CHOPSHAD		;MAKE SOME SHADOWS

	MOVE	@CHOPCNT,A6,W
	INC	A6
	MOVE	A6,@CHOPCNT,W

	JSRP	CHOPPOWR		;POWER THIS BEAST UP
	MOVI	>FFFF4000,A1
	CALLA	CARYVEL
	MOVK	20,B0
	MOVK	32,B1
	CALLA	RANGRAND
	SLEEPR	A0
	SOUND1	CHOPCRUZ
	MOVI	[6,0],A11
	MOVI	1000H,A9
	CALLR	CHOPXACC
	SLEEP	150
	CLR	A1
	CALLA	CARYVEL
	JRUC	CHOPL0
CHOPA1D
	CALLR	CHXVEL
CHOPL0
	CALLA	SELCTPLY	;GET A PLAYER TO GO FOR
CHOPL	
	MOVK	10,A9
CHOPALUP
	CALLR	CHOPVEL		;GET CHOPPER  VELOCITY
	SLEEP	1
	DSJ	A9,CHOPALUP
	CALLA	CARSCRTST
	JRNZ	CHOPOFF   	;NZ = OFF SCREEN	 
	MOVI	400,A0
	CALLA	RANDPER
	JRNC	CHOPL1
	CALLA	RANDOM
	SLL	31,A0
	SRL	26,A0
	ADDI	PILTAB,A0
	MOVE	*A0,A1,L
	MOVE	*A8(OFLAGS),A4,W
	CALLA	ANI
CHOPL1
	CALLR	CHFIRE
	MOVE	@CHOPRB,A0,W
	CALLA	RANDPER
	JRNC	CHOPL2
	CALLR	CHROCK
CHOPL2
	MOVE	@CHOPRB,A0,W
	CALLA	RANDPER
	JRNC	CHOPL
	CALLR	CHBOMB
	JRUC	CHOPL
*
*PILOT ANIMATION TABLE
*
PILTAB	.LONG	pilot5,pilot2
*OFFSCREEN, TURN AROUND
CHOPOFF:
	SLEEP	40		;TAKE A LITTLE TIME OUT
	CALLA	CARSCRTST
	JRZ	CHOPL   	;NZ = OFF SCREEN	 

	CALLR	CHXVEL
	MOVI	700,A0		 ;CHANGE ALTITUDE ?
	CALLA	RANDPER		 
	JRNC	CHOPFL 		;NO
	MOVI	150,A0
	CALLA	RANDU
	ADDK	20,A0		;GET RANDOM ALTITUDE
	MOVI	>30000,A2 	;Y SPEED
	CALLA	DFRMGRND
	CMP	A1,A0
	JRN	CHOPOF1
	NEG	A2
CHOPOF1
	MOVE	A2,A1	
	MOVE	A0,A9
	JSRP	CHUPDWN
CHOPOF2
	MOVK	20,A9 		;TIME OUT TO GET A NEW VELOCITY
CHOPFL
	CALLA	CARSCRTST
	JRZ	CHOPA1D
	CALLR	CHOPVEL
	SLEEP	2
	DSJS	A9,CHOPFL
	CALLR	CHXVEL		;GET NEW VELOCITY, WE MAY BE LOST
	JRUC	CHOPOF2
*
*BLINK CHOPPER LIGHT
*
CHOPLIT:
	MOVI	HELWHT,A0
	CALLA	FINDPAL
	JAZ	SUCIDE
	MOVE	A0,A8		;KEEP PALETTE POSITION
*
*PASS THE PALETTE # IN A8 HERE
CHOPLITG:
	SLL	24,A8
	SRL	16,A8
	ADDI	226,A8		;ADD IN EYE COLOR VALUE
CL0LP
	MOVI	CCOLTAB,A9	;INITIALIZE COLOR TABLE
CLILP:
	MOVE	A8,A1
	MOVK	1,A2
	MOVE	A9,A0
	CALLA	PALSET
	SLEEP	5
	ADDK	16,A9
	CMPI	CCOLTEND,A9
	JRHS	CL0LP
	JRUC	CLILP
*
*                R        G         B
*CHOP LIGHT COLOR TABLE
*0=END
CCOLTAB
      .WORD	0		       		;BLACK
      .word   (5*32*32)+(0 *32)+(  0 )  	;RED
      .word   (10*32*32)+(0*32)+(  0 )  	;WHITE
      .word   (15*32*32)+(0 *32)+( 0 )  
      .word   (20*32*32)+(0 *32)+( 0 )  
      .word   (25*32*32)+(0*32)+( 0)  
      .word   (28*32*32)+(0 *32)+( 0 ) 
      .word   (31*32*32)+(0 *32)+( 0 ) 
      .word   (31*32*32)+(31*32)+( 31 )  	;WHITE
CCOLTEND
*
*GET CHOPPER XVEL AND FLIP
*
CHXVEL
	PUSH	A11
	MOVE	*A8(OXPOS),A1,W
	CMPI	>FFFF8000,A1,W	 ;ZERO EXTEND TO C000
	JRLO	CHXV1
	CMPI	>FFFFC000,A1,W
	JRHI	CHXV1
	ZEXT	A1,W
CHXV1
	MOVE	@WORLDTL,A2,W
	ADDI	256,A2		;ADJUST FOR CENTER SCREEN
	CMPI	>FFFF8000,A2,W
	JRLO	CHXV2
	CMPI	>FFFFC000,A2,W
	JRHI	CHXV2
	ZEXT	A2,W
CHXV2
	SUB	A2,A1
	PUSH	A1
	SRA	31,A1		;SIGN EXTEND DOWNWARD
	MOVE	*A8(OFLAGS),A2,W
	XOR	A2,A1
	BTST	B_FLIPH,A1
	JRNE	CHXVEL0		 ;DONT FLIP IF FALLING BEHIND
	CALLA	CARSTOP
	CALLR	CHOPFLIP
CHXVEL0
	MOVE	@SCROLLX,A11,L
	ABS	A11
	MOVI	>40000,A0
	CALLA	RANDU
	PUSH	A8
	MOVE	A10,A8
	CALLA	DFRMGRND
	PULL	A8
	CMPI	50,A1
	JRHI	CHXVEL1
	ADDI	>20000,A0	;COOK ON LOW PASS
CHXVEL1
	ADDI	>40000,A0
	ADD	A0,A11
	PULL	A1		;GET BACK DISTANCE
	ABS	A1
	CMPI	600,A1		;WAYS OFF?
	JRLS	CHXVEL2
	CMPI	>80000,A11
	JRHS	CHXVEL2
	MOVI	>80000,A11
CHXVEL2
	MOVI	8000H,A9
	CALLR	CHOPXACC
	PULL	A11
	RETS
*
*Y ADJUST CHOPPER
*A1=YVEL
*A9=DFRMGRND GOAL
*
CHUPDWN
	CALLA	CARYVEL
	CLR	A1
	CALLA	CARZVEL		;NO Z VELOCITY
CHUDLP
	CALLA	DFRMGRND
	SUB	A9,A1
	ABS	A1
	CMPI	8,A1
	JRLS	CHUDX
	CALLR	CHFIRE
	CALLR	CHROCK
	SLOOP	2,CHUDLP
CHUDX
	CLR	A1
	CALLA	CARYVEL		 ;CUT Y VELOCITY
	RETP
*
*CHOPPER BULLET FIRE
*
CHFIRE
	MMTM	SP,A1,A7,A8,A10
	CALLA	CARSCRTST
	JRNZ	CHFIREX   		;NZ = OFF SCREEN	 
	MOVE	A10,A8
	CALLA	DFRMGRND
	CMPI	50,A1
	JRGT	CHFIRE1
	MOVI	[23,81],A10
	CREATE	0EBH,EDBULLET
	JRUC	CHFIREX
CHFIRE1
	MOVI	[30,75],A10		;FIRE A STRAIGHT SHOT
	CREATE	0EBH,EBULL45
CHFIREX
	MMFM	SP,A1,A7,A8,A10
	RETS
*
*CHOPPER BOMB DROP
*
CHBOMB
	MMTM	SP,A1,A7,A8,A10
	CALLA	CARSCRTST
	JRNZ	CHBOMBX   		;NZ = OFF SCREEN	 

	SWAP	A10,A8
	CALLA	DFRMGRND
	CMPI	80,A1
	JRLT	CHBOMBX			;NOT HIGH ENOUGH FOR BOMBING
	MOVE	@P1DATA+POBJ,A0,L	;LOOKING FOR PLAYER 1
	CALLR	BOMBPLY			;SEE IF PLAYER IN SIGHT
	JRC	CHBOMB2

	MOVE	@P2DATA+POBJ,A0,L	;LOOKING FOR PLAYER 1
	CALLR	BOMBPLY			;SEE IF PLAYER IN SIGHT
	JRNC	CHBOMBX
CHBOMB2
	SWAP	A10,A8
	MOVI	pilot1,A1		;FLIP THE DUDE OFF
	MOVE	*A8(OFLAGS),A4,W
	CALLA	ANI
CHBOMB3
	MOVE	A10,A8
	MOVI	[23,81],A10
	CREATE	0EBH,EBOMBP
CHBOMBX
	MMFM	SP,A1,A7,A8,A10
	RETS
*
*DROP A BOMB REGARDLESS
*
BOMBLO
	MMTM	SP,A1,A7,A8,A10
	JRUC	CHBOMB3

**************************************************************************
*                                                                        *
* EBOMBP - ENEMY BOMB PROCESS
* A8  = OBLOCK PTR OF ENEMY						 *
* A10 = XY OFFSETS							 *
*                                                                        *
**************************************************************************
* A8  = PTR TO OBLOCK OF THE OBJECT DOING THE FIRING			 *
* A10 = BULLET OFFSET							 *
* A11 = PLAYER DATA PTR(0 = ENEMY NOT A PLAYER)				 *
* A14 = OBJECT INIT. TABLE						 *
* B0  = RELEASE SOUND TO MAKE						 *
* B4  = PLACE TO JUMP TO FINISH ANIMATION OF PROJECTILE			 *
EBOMBP
	CLR	A11
	MOVI	BOMBINIT,A14
	MOVI	BMBSND,B0
	MOVI	EBMBP1,B4
	JAUC	GBULLET
EBMBP1
	MOVI	BEOID|BOMBID,A1
	MOVE	A1,*A8(OID)		;STUFF YOUR ID
	MOVE	*A0(OXVEL),*A8(OXVEL),L
	MOVI	>20000,A1
	MOVE	A1,*A8(OYVEL),L
	MOVE	A8,A0			;PTR FOR INSERTION TO DATA BLOCK
	CALLA	INSOBJ			;PUT THE OBJECT ON THE LIST	 
	MOVI	ENEMY_BULLETS,A1
	CALLA	ADDSUPP
	CALLA	SHADST			;GIVE THIS GUY A SHADOW
BFALL
	SLEEP	3
	MOVE	*A8(OYVEL),A0,L		;EFFECT OF GRAVITY
	ADDI	>5000,A0
	MOVE	A0,*A8(OYVEL),L
	CALLA	DFRMGRND
	CMPI	0,A1
	JRGT	BFALL			;IN THE AIR STILL

*BLOW THIS BABY UP FOLKS !!!!!
	CLR	A0
	MOVE	A0,*A8(OPLINK),L	;ZERO OUT OPLINK
	MOVE	A8,A0
	CALLR	BCOLL1
	JAUC	SUCIDE			;WERE OUTA HERE

BMBSND	.WORD	>F310,>20,>8093,0	;FALLING BOMB
*
*BOMB COLLISION
*A8 = PTR TO OTHER THING 
*A0 = PTR TO LAND MINE
*
BCOLL1 
	MOVE	A0,A8
	CALLA	DFRMGRND
	CMPI	10,A1
	JRLE	BCOL1NEX
	MOVI	NEXAIR,A7
	JRUC	BCOL1EXG
BCOL1NEX
	MOVI	NEXPLODE,A7    		;NORMAL EXPLOSION
BCOL1EXG
	MOVI	REXPID,A1		;EXPROSION PROCESS I.D.	
	MOVE	*A0(OZPOS),A2,W		;LAND MINE Z
	MOVE	*A0(OFLAGS),A9,W
	SLL	16,A9
	MOVX	A2,A9			;A9 = OFLAGS:OZPOS
	MOVE	*A0(OXPOS),A11,W
	MOVE	*A0(OYVAL),A2,L

	MOVE	*A0(OID),A10,W		;A10 = BOMB I.D.
	MOVY	A2,A11
	MOVE	A0,B0
	CALLA	GETPRC			;CREATE THE ROCKET EXPLOSION
	MOVE	B0,A0
	MOVE	*A0(OPLINK),A0,L
	JRZ	BCOLL4
	CALLA	KILL
BCOLL4
	MOVE	B0,A0
	MOVI	ENEMY_BULLETS,A1
	CALLA	DELSUPP
	CALLA	DELSOBJ
	CLR	A0
	INC	A0
	RETS

