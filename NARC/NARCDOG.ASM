*
*DOGBITE
*LETS BITE THE DUDE
*A0=CLOSEST PLAYER DATA AREA
*A1=DELTA X
*A2=DELTA Z
*A3=ABS DX
*A4=ABS DZ
*A8=DOG OBJECT
*A13=DOG PROCESS
*
DOGBITE:	
	MOVI	250,A0
	CALLA	RANDPER
	JRNC	DGBT0
	MOVI	DGRR1SND,A0
	JRUC	DGBT1
DGBT0	
	MOVI	DGRWLSND,A0	;GROWL IT UP
DGBT1
	CALLA	ONESND

	MOVE	A1,A1		;FLIP THE DUDE PROPERLY
	JRN	DOGBIT0
	CALLA	NOYFLP
	JRUC	DOGBIT1
DOGBIT0:
	CALLA	YFLP
DOGBIT1:
	CALLA	PSTOP		;STOP THE DUDE

*GET VELOCITY
*FLY, CHECK FOR BITE
*LAND
*CHECK FOR LIE DOWN	
	BTST	BDEAD,A7	;PLAYER DOWN?
	JREQ	DOGBIT2		;NOPE
	CMPI	35,A3		;DOG AT THE END OF DUDE'S BODY
	JRHS	DOGDRG		;NO
DGBT2
	MOVI	7,A0
	CALLA	RANDU
	SLL	5,A0
	ADDI	LOWBTAB,A0
	MOVE	*A0,A9,L
	JRUC 	BITSHT2
*LOW ATTACK TABLE
LOWBTAB
	.LONG	GRR1,GRR2,TEAR1,TEAR2,TUG,DRAG,TUG,GRR2
*DRAG HIM AWAY
DOGDRG:
	CALLA	GETCPLY	      	;SAVE PLAYER STUFF
	MOVE	A0,A10	
DOGDRG0
	MOVI	DRAG,A9		;DRAG ANIMATION
DOGDRGL
	MOVK	4,A1
	JSRP	FRANIM
	MOVE	A0,A11		;SAVE SLEEP TIME
	JRC	DOGDRG0
DOGDRG1
	MOVE	*A10(PFLAGS),A7,L
	BTST	BDEAD,A7	;PLAYER STILL DOWN?
	JREQ	GPLAYR		;NOPE
	MOVI	16,A0
	CALLA	RANDPER		;QUIT DRAGGING ?
	JRC	GPLAYR		;YIP, ITS GETTING OLD
	MOVE	*A10(POBJ),A0,L	;PLAYER OBJECT
	CLR	A1		;NO X OFFSET
	MOVI	10,A3		;Z IN FRONT
	CALLA	XZCNTCK		;STILL IN RANGE
	CMPI	45,A3
	JRHS	GPLAYR			
	PUSH	A0
	MOVI    >18000,A0	;GET RANDOM DRAG FACTOR	
	CALLA	RANDU
	ADDI	>8000,A0
	MOVE	A0,A3
	PULL	A0
	MOVE	A1,A1
	JRN	DOGDRG2
	NEG	A3
DOGDRG2
	MOVE	*A0(OXVAL),A2,L
	ADD	A3,A2
	MOVE	A2,*A0(OXVAL),L
	MOVE	*A0(OSHAD),A4,L		;DRAG HIS STUPID SHADOW
	JREQ	DOGDRG3			;NO SHADOW...
	MOVE	A2,*A4(OXVAL),L
DOGDRG3
	MOVE	*A10(PLOBJ),A0,L	;GET OTHER HALF
	MOVE	*A0(OXVAL),A2,L
	ADD	A3,A2
	MOVE	A2,*A0(OXVAL),L
	MOVE	*A8(OXVAL),A2,L
	ADD	A3,A2
	MOVE	A2,*A8(OXVAL),L
	SLEEP	 1
	DSJ	A11,DOGDRG1	
	JRUC	DOGDRGL

DOGBIT2:
*CHECK FOR A JUMP SHOT
	BTST	BJUMP2,A7		;IN AIR ?
	JREQ	DOGBIT3	  		;NO
	MOVI	200,A0
	CALLA	RANDPER			;JUMPER?
	JRC	DOGJMP			;NOPE

*SHORT JUMP	
*GET TAKEOFF FRAMES
DOGBIT3
	MOVI	QKLEAP,A9	;WERE IN BACK DO SIDE ATTACK
	MOVE	A2,A2		;TEST DELTA Z
	JRNN	BITSHT0
	CALLA	SHADST		;START A SHADOW FOR UNSHADOWED FRAMES
	MOVI	QKATTACK,A9
BITSHT0
	MOVK	1,A1
	JSRP	FRANIM	  	;DO YOUR TAKEOFF
	MOVI	>30000,A0
	CALLA	RANDU
	MOVE 	*A8(OFLAGS),A1,W
	BTST	B_FLIPH,A0
	JRZ	BITSHT1
	NEG	A0
BITSHT1
	MOVE	A0,*A8(OXVEL),L
BITSHT2:
	CALLA	GETCPLY
	MOVE	*A0(PFLAGS),A7,L
	BTST	BJUMP2,A7		;IN AIR ?
	JREQ	BITSHT3
	BTST	BDEAD,A7		;PLAYER DOWN?
	JRNE	BITSHT3			;YES
	MOVE	A0,A10			;SAVE PLAYER DATA PTR
	MOVE	*A0(PLOBJ),A0,L 	;PLAYER OBJECT LEGS
	MOVE	*A0(OYPOS),A2,W
	MOVE	*A0(OSIZEY),A1,W
	ADD	A1,A2
	MOVE	*A8(OYPOS),A1,W
	ADDI	8,A1			;FUDGE FACTOR
	CMP	A1,A2			;ARE WE HIGH ENOUGH TO BITE
	JRLS	BITSHT3X		;NO...
	MOVI	35,A5
	MOVI	10,A6
	CALLA	XZCHK			;ARE WE IN XZ POSITION?
	JRNC	BITSHT3X		;NOPE...
	MOVE	A10,A0
BITSHT3	
	MOVE	@WAVE,A1,W
	CMPI	1,A1		;WAVE 1?
	JRNE	BITSHT3A	;NO
	CALLA	BOZCHK
	JRNC	BITSHT3A		;NO BOZO TARGET
	MOVE	A0,A10
	MOVI	200,A0
	CALLA	RANDPER
	JRNC	BITSHT3X		;BITE SOME OF THE TIME
	MOVE	A10,A0
BITSHT3A	
	MOVE	A0,A2
	CALLA	KNIFEHIT
	CALLR	DGBLOOD			;MAKE DOG BITE BLEED
BITSHT3X:
	MOVE	A8,A0			;GET RID OF SHADOW FOLKS
	CALLA	DELSHAD
	MOVK	1,A1			;DO YOUR LANDING FRAMES
	JSRP	FRANIM
	JRUC	GPLAYR	  		;START OVER AGAIN

*DOG JUMP SHOT AT PLAYER
DOGJMP:
	MOVI	FATTACK,A9
	MOVE	A2,A2		;TEST DELTA Z
	JRN	DOGJMP0
	MOVI	LEAP,A9		;WERE IN BACK DO SIDE ATTACK
DOGJMP0
	MOVK	1,A1
	JSRP	FRANIM	  	;DO YOUR TAKEOFF
	MOVE	A9,*A13(ETEMP2),L	;SAVE RESTART POINT	
	CALLA	GETCPLY
	MOVE	A0,A11		;SAVE DUDES POINTER
	MOVE	*A0(POBJ),A0,L 	;PLAYER OBJECT
	CALLA	XZCHK	       
	MOVE	*A0(OXVEL),A5,L	;GET PLAYER VELOCITY
	MOVE	*A0(OZVEL),A6,L
	SLL	11,A1		;ADJUST X VELOCITY
	SLL	11,A2		;GET ZVEL
	MOVI	500,A0
	CALLA	RANDPER
	JRNC	DOGJMP1
	ADD	A5,A1		;ADD PLAYER VELOCITY
	ADD	A6,A2
DOGJMP1:
	MOVI	>30000,A0	;GET Y VELOCITY
	CALLA	RANDU
	ADDI	>FFFA0000,A0
	
	MOVE	A1,*A8(OXVEL),L
	MOVE	A2,*A8(OZVEL),L
	ADD	A2,A0
	MOVE	A0,*A8(OYVEL),L
	CALLA	SHADST
	CALLA	SHVELCPY
	CLR	A10			;BITE FLAG
	MOVE	A1,A1			;FLIP THE DUDE PROPERLY
	JRN	DOGJMP2
	CALLA	NOYFLP
	JRUC	JUMP1
DOGJMP2:
	CALLA	YFLP
JUMP1:
	MOVE	*A13(ETEMP2),A9,L	;GET RESTART POINT	
JUMPL:
	MOVK	4,A1
	JSRP	FRANIM
	JRC	JUMP1
	SLEEP	3
	MOVE	A10,A10
	JREQ	JUMP2			;ALEADY BEEN BITTEN

*CHECK FOR BITE
	MOVE	*A11(PLOBJ),A0,L 	;PLAYER OBJECT LEGS
	MOVI	8,A4			;Y OFFSET
	MOVI	20,A5			;XRANGE
	MOVI	10,A6
	CALLA	XYZCK			;ARE WE IN POSITION
	JRC	JMPBIT			;BITE THE DUDE

	MOVE	*A11(POBJ),A0,L 	;PLAYER OBJECT
	MOVI	8,A4			;Y OFFSET
	MOVI	20,A5			;XRANGE
	MOVI	10,A6
	CALLA	XYZCK			;ARE WE IN POSITION
	JRNC	JUMP2
JMPBIT
	INC	A10
	MOVE	A11,A2			;GIVE PLAYER KNIFE HIT
	CALLA	KNIFEHIT
	MOVE	A11,A0
	CALLR	DGBLOOD			;MAKE DOG BITE BLEED
JUMP2
	CALLA	ZCHK			;MAKE SURE Z IN BOUNDS
	MOVE	*A8(OYVEL),A0,L		;EFFECT OF GRAVITY
	ADDI	>0B000,A0,L
	MOVE	A0,*A8(OYVEL),L
	MOVE	*A8(OZVEL),A1,L		;REMOVE Z COMPONENT
	SUB	A1,A0
	JRN	JUMPL			;UPWARD VEL, NO LAND
	CALLA	DFRMGRND
	CMPI	10,A1
	JRGT	JUMPL			;IN THE AIR STILL

*LANDING ANIMATION
	CALLR	FNANI			;FIND NEXT ANIMATION
	CALLA	PSTOP			;STOP THE SUCKER   
	MOVE	A8,A0			;OBJECT IN A0 FOR SETZPOS
	CALLA	SETZPOS			;ADJUST THE MUTHA
	CALLA	DELSHAD			;RESET YOUR SHADOW
*	CALLA	SHADST
	MOVK	1,A1			;DO YOUR LANDING FRAMES
	JSRP	FRANIM
	JRUC	GPLAYR	  		;START OVER AGAIN
