*
*CHECK X Y Z
*A0=OBJECT SEEKED
*A4=Y OFFSET
*A5=XRANGE
*A6=ZRANGE
*RETURNS CARRY IF IN RANGE
*A1,A2,A3,A4 AS XZCHK
XYZCK:
	MOVE	*A0(OFLAGS),A2,W
	SLL	28,A2
	JREQ	XYZFL			;OBJECT NOT ACTIVE
	MOVE	*A0(OYPOS),A2,W
	MOVE	*A0(OSIZEY),A1,W
	ADD	A2,A1
	MOVE	*A8(OYPOS),A3,W
	ADD	A4,A3			;FUDGE FACTOR
	CMP	A3,A1			;ARE WE IN Y RANGE ?
	JRHS	XYZFL			;OUT OF RANGE
	CMP	A3,A2
	JRLS	XYZFL
	CALLA	XZCHK			;ARE WE IN POSITION
	RETS
XYZFL	
	CLRC
	RETS
*
*DOG BLOOD START
*A8=DOG, A0=PLAYER DATA 
*
DGBLOOD:
